void Conv2D(){
	for(int imz=0; imz<num_filters; imz++){
		for(int imx=0; imx<24; imx++){
			for(int imy=0; imy<24; imy++){
				for(int fx=4; fx>0; fx--){
					for(int fy=4; fy>0; fy--){
						for(int fz=0; fz<num_filters; fz++){
							fmap1[imx][imy][imz] += (input[imx+fx][imy+fy][imz+fz] * wmap1[fx][fy][fz])+bmap1[imz];
							//printf("%f %f %f\n",input[imx+fx][imy+fz][imz],wmap1[fx][fy][fz],fmap1[imx][imy][imz]);

							//Activation ReLU
							if(fmap1[imx][imy][imz]<0)
								fmap1[imx][imy][imz]=0;

						}
					}
				}
			}
		}
	}	

}

void Maxpooling2D(){
	float max=0;
	for(int imz=0; imz<num_filters; imz++){
		for(int imx=0; imx<24; imx=imx+2){
			for(int imy=0; imy<24; imy=imy+2){
				max = 0;
				for(int fx=0; fx<2; fx++){
					for(int fy=0; fy<2; fy++){
						if(fmap1[imx+fx][imy+fy][imz]>max){
							max = fmap1[imx+fx][imy+fy][imz];
						}
					}
				}
				fmap2[imx/2][imy/2][imz] = max;
			}
		}
	}	


}

void Flatten(){
	for(int i=0; i<12; i++){
		for(int j=0; j<12; j++){
			for(int k=0; k<num_filters; k++){
				//for n x m x p ??????+????+??

				dense[(12*i+j)*num_filters+k] = fmap2[i][j][k];
				//printf("%d %d %d %d %.2f\n",i,j,k,(12*i+j)*num_filters+k, fmap2[i][j][k]);
				
			}
		}
	}
}

void Dense(){
	float sum=0;
    double y=0;
	for(int j=0; j<10; j++){
        y=0;
		for(int i=0; i<12*12*num_filters; i++){
			y += dense[i] * wmap2[j][i];
		}
		y += bmap2[j];
        output[j] = (float)pow(e,y);
        sum += output[j];
        //printf("%d %f\n",j,output[j]);
	}

	//Softmax
	for(int j=0; j<10; j++){
		output[j] = output[j]/sum;
	}

}

